CREATE POLICY "Allow public select on law_firms" ON law_firms FOR SELECT USING (true);
CREATE POLICY "Allow public insert on law_firms" ON law_firms FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update on law_firms" ON law_firms FOR UPDATE USING (true);
CREATE POLICY "Allow public delete on law_firms" ON law_firms FOR DELETE USING (true);

-- Policies for 'judges' table
CREATE POLICY "Allow public select on judges" ON judges FOR SELECT USING (true);
CREATE POLICY "Allow public insert on judges" ON judges FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update on judges" ON judges FOR UPDATE USING (true);
CREATE POLICY "Allow public delete on judges" ON judges FOR DELETE USING (true);

-- Policies for 'lawyers' table
CREATE POLICY "Allow public select on lawyers" ON lawyers FOR SELECT USING (true);
CREATE POLICY "Allow public insert on lawyers" ON lawyers FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update on lawyers" ON lawyers FOR UPDATE USING (true);
CREATE POLICY "Allow public delete on lawyers" ON lawyers FOR DELETE USING (true);

-- Policies for 'cases' table
CREATE POLICY "Allow public select on cases" ON cases FOR SELECT USING (true);
CREATE POLICY "Allow public insert on cases" ON cases FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update on cases" ON cases FOR UPDATE USING (true);
CREATE POLICY "Allow public delete on cases" ON cases FOR DELETE USING (true);
